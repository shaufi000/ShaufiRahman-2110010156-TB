    private void tampilkan() {
    int row = model.getRowCount();
    for (int a = 0; a < row; a++) {
        model.removeRow(0);
    }

    try {
        Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/dbobjek_wisata", "root", "");
        ResultSet rs = cn.createStatement().executeQuery("SELECT * FROM kategori");

        while (rs.next()) {
            String data[] = {rs.getString(1), rs.getString(2)};
            model.addRow(data);
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Koneksi ke database gagal: " + ex.getMessage());
        Logger.getLogger(Kategori.class.getName()).log(Level.SEVERE, null, ex);
    }
}
    
