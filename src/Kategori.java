    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
    int result = fileChooser.showSaveDialog(this);

    if (result == JFileChooser.APPROVE_OPTION) {
        File selectedFile = fileChooser.getSelectedFile();

        try {
            String fileFormat = getFileExtension(selectedFile);

            FileWriter out = new FileWriter(selectedFile, true);

            for (int row = 0; row < model.getRowCount(); row++) {
                String idKategori = model.getValueAt(row, 0).toString();
                String namaKategori = model.getValueAt(row, 1).toString();

                if ("txt".equalsIgnoreCase(fileFormat)) {
                    out.write(idKategori + "\t" + namaKategori);
                    out.write(System.getProperty("line.separator"));
                } else if ("csv".equalsIgnoreCase(fileFormat)) {
                    out.write(idKategori + "," + namaKategori);
                    out.write(System.getProperty("line.separator"));
                }
            }

            out.close();

            JOptionPane.showMessageDialog(this, "Data dalam tabel berhasil disimpan", "Export Success",
                    JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException e) {
            e.printStackTrace(); // Handle the exception appropriately
            JOptionPane.showMessageDialog(this, "Data dalam tabel gagal disimpan", "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_SimpanActionPerformed

    private void CariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CariActionPerformed
        // TODO add your handling code here:
        try {
        Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/dbobjek_wisata", "root", "");
        String keyword = tfCari.getText();
        String sql = "SELECT * FROM kategori WHERE idKategori LIKE ?";

        try (PreparedStatement ps = cn.prepareStatement(sql)) {
            ps.setString(1, "%" + keyword + "%");

            ResultSet rs = ps.executeQuery();
            model.setRowCount(0);

            while (rs.next()) {
                String data[] = {rs.getString(1), rs.getString(2)};
                model.addRow(data);
            }
        }

        cn.close();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Koneksi ke database gagal: " + ex.getMessage());
        Logger.getLogger(Kategori.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_CariActionPerformed

